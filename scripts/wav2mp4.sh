#!/usr/bin/env bash

ARGS=()
ARGS="${@}"

__TMP_IMAGE_NAME=__SPEAKER_IMAGE.PNG
__OUT_NAME=
__IN_NAME=

__USE_DIFFERENT_IMAGE=false
__OTHER_IMAGE_NAME=

__help () {
  echo "wav2mp4.sh --- Convert audio to a video for upload. "
  echo " "
  echo "-i <audio.wav>        The input audio."
  echo " "
  echo "-o <video.mp4>        The output for the video."
  echo " "
  echo "--image <image.png>   Image to use. If non is provided a standard image"
  echo "                      will be used."
  echo " "
  echo ""
  exit
}

__do_cleanup () {
  rm $__TMP_IMAGE_NAME
}

___do_convertion () {
  ffmpeg -loop 1\
         -i "$__TMP_IMAGE_NAME"\
         -i "$__IN_NAME"\
         -c:v libx264 -tune stillimage\
         -c:a aac -b:a 256k -pix_fmt yuv420p\
         -vf "pad=ceil(iw/2)*2:ceil(ih/2)*2" -shortest\
         "$__OUT_NAME"
}


__parse_args() {
  if [[ -z "$1" ]]
  then
    echo "Try --help or -h."
    exit 1
  fi
  
  
  while [[ $# -gt 0 ]]
  do
    eval key="${1}"
    
    case "${1}" in
      -i)
      __IN_NAME="$2"
      shift
      shift
      ;;
      -o)
      __OUT_NAME="$2"
      shift
      shift
      ;;
      --image)
      __USE_DIFFERENT_IMAGE=true
      __OTHER_IMAGE_NAME="$2"
      shift
      shift
      ;;
      -h|--help)
      __help
      exit
      shift
      ;;
      *)
      __help
      exit 1
      shift
      ;;
      --)
      shift
      break
      ;;
    esac
  done
}


